use anyhow::{Error, Result};
use std::str::FromStr;

#[derive(Debug, Copy, Clone, Eq, PartialEq)]
enum Throw {
    Rock,
    Paper,
    Scissors,
}

impl FromStr for Throw {
    type Err = Error;

    fn from_str(s: &str) -> std::result::Result<Self, Self::Err> {
        Throw::from_char(s.chars().next().unwrap())
    }
}

impl Throw {
    fn score(&self) -> i32 {
        match self {
            Throw::Rock => 1,
            Throw::Paper => 2,
            Throw::Scissors => 3,
        }
    }

    fn beats(&self, other: &Self) -> bool {
        match self {
            Throw::Rock => *other == Throw::Scissors,
            Throw::Paper => *other == Throw::Rock,
            Throw::Scissors => *other == Throw::Paper,
        }
    }

    fn from_char(ch: char) -> std::result::Result<Self, Error> {
        match ch {
            'A' => Ok(Throw::Rock),
            'X' => Ok(Throw::Rock),
            'B' => Ok(Throw::Paper),
            'Y' => Ok(Throw::Paper),
            'C' => Ok(Throw::Scissors),
            'Z' => Ok(Throw::Scissors),
            _ => Err(Error::msg("Unmatched")),
        }
    }
}

#[derive(Debug)]
struct Hint {
    they: char,
    we: char,
}

impl Hint {
    fn score(&self) -> i32 {
        let our_throw = Throw::from_char(self.we).unwrap();
        let their_throw = Throw::from_char(self.they).unwrap();

        if their_throw == our_throw {
            3 + our_throw.score()
        } else if our_throw.beats(&their_throw) {
            6 + our_throw.score()
        } else {
            our_throw.score()
        }
    }
}

impl FromStr for Hint {
    type Err = Error;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let mut parts = s.split(" ");
        let they = parts.next().unwrap().chars().next().unwrap();
        let we = parts.next().unwrap().chars().next().unwrap();
        Ok(Hint { they, we })
    }
}

enum Outcome {
    LOSE,
    DRAW,
    WIN,
}

impl Outcome {
    fn from_char(ch: char) -> std::result::Result<Self, Error> {
        match ch {
            'X' => Ok(Outcome::LOSE),
            'Y' => Ok(Outcome::DRAW),
            'Z' => Ok(Outcome::WIN),
            _ => Err(Error::msg("Unmatched")),
        }
    }

    fn score(&self) -> i32 {
        match self {
            Outcome::LOSE => 0,
            Outcome::DRAW => 3,
            Outcome::WIN => 6,
        }
    }
}

#[derive(Debug)]
struct Goal {
    they: char,
    outcome: char,
}

impl Goal {
    fn plan(&self) -> Throw {
        let outcome = Outcome::from_char(self.outcome).unwrap();
        let their_throw = Throw::from_char(self.they).unwrap();
        match outcome {
            Outcome::LOSE => match their_throw {
                Throw::Rock => Throw::Scissors,
                Throw::Paper => Throw::Rock,
                Throw::Scissors => Throw::Paper,
            },
            Outcome::DRAW => their_throw,
            Outcome::WIN => match their_throw {
                Throw::Rock => Throw::Paper,
                Throw::Paper => Throw::Scissors,
                Throw::Scissors => Throw::Rock,
            },
        }
    }

    fn score(&self) -> i32 {
        let outcome = Outcome::from_char(self.outcome).unwrap();

        outcome.score() + self.plan().score()
    }
}

impl FromStr for Goal {
    type Err = Error;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let mut parts = s.split(" ");
        let they = parts.next().unwrap().chars().next().unwrap();
        let we = parts.next().unwrap().chars().next().unwrap();
        Ok(Goal { they, outcome: we })
    }
}

